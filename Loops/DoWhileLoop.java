public class DoWhileLoop {
    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i<11);

        // condition is true or false no matter atleast one time code runs
        int j = 10;
        do {
            System.out.println("Debojyoti");
            j++;
        } while (j>15);
    }
}
